#include <iostream>
using namespace std;

int main() {
    // Declare a variable to hold the user input
    int num;

    // Prompt the user for an integer
    cout << "Enter an integer: ";
    cin >> num;

    // Double the input
    int doubled_num = num * 2;

    // Display the doubled value
    cout << "Doubled value: " << doubled_num << endl;

    return 0;
}
